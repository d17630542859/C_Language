    int a, b, c, t;
    scanf("%d%d%d%d", &a, &b, &c, &t);
    printf("%.2f\n", (1.0 - ((1.0 / a) + (1.0 / b)) * t) / (1.0 / a + 1.0 / b - 1.0 / c) + t);



